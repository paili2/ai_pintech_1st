package exam02;

public class Ex06 {
    static int num1;
    static double num2;
    static boolean result;
    static String str;

    public static void main(String[] args) {
        // 변수만 선언하고 값을 대입하지 않으면
        System.out.println(num1); // 정수는 0
        System.out.println(num2); // 실수는 0.0
        System.out.println(result); // 불리언은 false
        System.out.println(str); //null
    }
}
